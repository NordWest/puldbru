<br>
На первой странице вы можете выбрать звезду, задав ее номер по ADS или WDS,
или воспользоваться полным списком визуально-двойных звезд, наблюдавшихся 
в Пулкове. Список содержит: номер звезды, компоненты, примечание, название 
измерительной машины (для фотогр. наблюдений), зв. величину и спектральный 
класс.

<br><br> 
Примечание 1: Основная масса пластинок измерялась на Аскорекорде. Часть измерений
выполнена на автоматической машине ФАНТАЗИЯ. Небольшое число пластинок измерено на
полуавтоматической машине ПАРСЕК и на сканере (UMAX PowerLook II).

<br><br> 
Примечание 2: Символ (N) в графе Note означает наличие комментариев, символ (*)
указывает, что звезда не отождествляется в каталоге WDS, например, это может быть 
двойная звезда, открытая в Пулкове.

