Условные обозначения:

<ul style="list-style-type:none">

    <li style="font-weight:normal"><b>Epoch
    </b> - момент наблюдений

    <li style="font-weight:normal"><b>JD
    </b> - момент наблюдений в Юлианских днях

    <li style="font-weight:normal"><b>S
    </b> - средневесовое значение углового расстояния

    <li style="font-weight:normal"><b>P
    </b> - средневесовое значение позиционного угла (эпоха наблюдений)

    <li style="font-weight:normal"><b>e(S)
    </b> - среднеквадратичная ошибка углового расстояния
	(0 означает, что ошибка не вычислялась)

    <li style="font-weight:normal"><b>e(P)
    </b> - среднеквадратичная ошибка позиционного угла
	(0 означает, что ошибка не вычислялась)

    <li style="font-weight:normal"><b>e1(S)
    </b> - среднеквадратичная ошибка углового расстояния, 
	вычисленная по 1 пластинке
	(0 означает, что ошибка не вычислялась)

    <li style="font-weight:normal"><b>e1(P)
    </b> - среднеквадратичная ошибка позиционного угла,
        вычисленная по 1 пластинке
	(0 означает, что ошибка не вычислялась)

    <li style="font-weight:normal"><b>N(S)
    </b> - количество пластинок, использованных для определения углового расстояния

    <li style="font-weight:normal"><b>N(P)
    </b> - количество пластинок, использованных для определения позиционного угла

    <li style="font-weight:normal"><b>N
    </b> - общее количество пластинок

    <li style="font-weight:normal"><b>Orient.
    </b> - метод ориентировки:

	<ul style="list-style-type:none">
	<li>1 - если ориентировка произведена по следу или по немым звездам, привязанным к следу соседней пластинки;
	<li>4 - если ориентировка произведена по паре каталожных звезд;
	<li>9 - если положение получено по пластинкам с разной	ориентировкой;
	</ul>

    <li style="font-weight:normal"><b>Meas.code
    </b> - код измерителя
	
